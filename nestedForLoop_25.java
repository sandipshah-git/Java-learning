public class nestedForLoop_25 {
    public static void main(String[] args){
        // nested loop = a loop inside another loop 
        //              use often with matrices and dsa

        for(int i=0;i<5;i++){
            for(int j=0;j<=3;j++){
                System.out.print(i + " ");
            }
            System.out.println();
        }
    }
}
