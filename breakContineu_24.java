public class breakContineu_24 {
    public static void main(String[] args){

        // break = break out of a loop (stop)
        // continue = skip corrent iteration of a loop (skip)

        for(int i=1;i<=10;i++){
            if(i==5){
                // break;
                continue;
            }
            System.out.print(i + " ");
        }


    }
}
